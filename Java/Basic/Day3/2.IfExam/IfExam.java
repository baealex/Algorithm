public class IfExam {

    public static void main(String[] args) {
        int su = 12;

        System.out.println("int: " + su);
        if (su % 2 == 0) {
            System.out.println("Drainage of 2");
        }

        if (su % 3 == 0) {
            System.out.println("Drainage of 3");
        }

        if (su % 4 == 0) {
            System.out.println("Drainage of 4");
        }
    }

}
