public class UnaryOperator {
    public static void main(String [] args) {
        int opr = 0;
        System.out.println(opr++);
        System.out.println(opr);
        System.out.println(++opr);
        System.out.println(opr);
        System.out.println(opr--);
        System.out.println(opr);
        System.out.println(--opr);
        System.out.println(opr);
    }
}